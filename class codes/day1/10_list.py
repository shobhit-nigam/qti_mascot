avengers = ["captain", "ironman", "wanda", "hulk", "hawkeye"]
xmen = ["wolverine", "magneto", 'deadpool']
marvel = ["spiderman" , avengers , xmen]
print("marvel =", marvel)
print("----------")
print("marvel[2] =", marvel[2])
print("----------")
print("marvel[2][2] =", marvel[2][2])
print("----------")
print("marvel[2][2][4:11] =", marvel[2][2][4:11])

